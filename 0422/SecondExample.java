/*
複数のコメントをいれられる
*/
//　コメントを１行書きたいときに使う

// プログラムコードに記述された命令文は上から下に実行される
public class SecondExample {  // classの後はファイル名をいれる
    // javaはmainから実行されるためmainは必須
    public static void main(String[] args){
        System.out.println("さようなら");  // 画面に指定した文字を表示する処理
        System.out.print("ほのかさん、");
        System.out.print("また明日");  
    }
}