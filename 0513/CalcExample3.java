public class CalcExample3 {
    public static void main(String args[]){
        int i;
        i = 11;
        i ++ ; // 11に+1する　→ 12
        i /= 2; // i / 2　→　12 / 2
        System.out.println("iの値は" + i);

        int j;
        j = i * i;
        System.out.println("jの値は" + j);

        int k = 2 + 3;
        int m = k * 2;
        System.out.println(m);
    }
}
